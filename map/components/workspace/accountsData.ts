// takes: the core accounts array and the Excel-imported accounts array
// does: concatenates both, then keeps the first occurrence of each account
//       name (case-insensitive) so a company can never appear twice
// returns: the combined, deduplicated AccountProfile array
export function getUniqueAccounts(
  coreData: AccountProfile[],
  excelData: AccountProfile[],
): AccountProfile[] {
  const seen = new Set<string>();
  const out: AccountProfile[] = [];
  for (const a of [...coreData, ...excelData]) {
    const key = a.account.trim().toLowerCase();
    if (!key || seen.has(key)) continue;
    seen.add(key);
    out.push(a);
  }
  return out;
}

